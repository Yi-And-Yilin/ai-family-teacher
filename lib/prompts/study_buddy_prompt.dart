/// 【核心设计理念】
/// - 讲解/批改时使用 B> 黑板前缀（流式输出适合逐步讲解）
/// - 出题时使用工具调用（tool calls），因为题目需要严格的 UI 组件和交互
/// - 无前缀的文本默认显示在聊天区（不需要 C>）

